class StringMethods
{
	public static void main(String args[])
	{
		String str="Sri Krishna";
		System.out.println("Upper"+str.toUpperCase());
		System.out.println("Lower"+str.toLowerCase());
		System.out.println("Lentgh"+str.length());
		System.out.println("Concatenate"+str.concat("! Hello"));
		System.out.println("Position"+str.charAt('a'));
		System.out.println("Index"+str.indexOf(' '));
	}
}