import java.util.*;
class DataType
{
  public static void main(String args[])
   {
    byte b=10;
    short s=20;
    int a=3;
    float f=25.5f;
    double d=333.5674;
    long l=4500000000L;
    char ch='a';
    boolean i= true;
    System.out.println(b);
    System.out.println(s);
    System.out.println(a);
    System.out.println(f);
    System.out.println(d);
    System.out.println(l);
    System.out.println(ch);
    System.out.println(i);
   }
}